package model;

public class Data {
    private String t, f;

    public Data(String t, String f) {
        this.t = t;
        this.f = f;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }
}